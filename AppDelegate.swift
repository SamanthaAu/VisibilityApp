        // Override point for customization after application launch.
        FirebaseApp.configure()
        
        let defaults = UserDefaults.standard
        
        let rateValue = ["rate" : 0.5]
        defaults.register(defaults: rateValue)
        
        let pitchValue = ["pitch" : 1.0]
        defaults.register(defaults: pitchValue)
        
        let appearanceValue = ["appearance" : "warm"]
        defaults.register(defaults: appearanceValue)
        
        return true
    }

